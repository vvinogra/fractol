#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*m;
	size_t			i;

	i = 0;
	m = (unsigned char *)s;
	while (i < n)
	{
		m[i] = 0;
		i++;
	}
}
